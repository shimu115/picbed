CREATE TABLE IF NOT EXISTS tokens (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    token_hash VARCHAR(64) NOT NULL UNIQUE,
    role VARCHAR(10) NOT NULL DEFAULT 'USER',
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP NULL
);

ALTER TABLE IF EXISTS tokens ADD COLUMN IF NOT EXISTS role VARCHAR(10) NOT NULL DEFAULT 'USER';
ALTER TABLE IF EXISTS tokens ADD COLUMN IF NOT EXISTS email VARCHAR(255) NULL;

CREATE TABLE IF NOT EXISTS images (
    id BIGINT AUTO_INCREMENT PRIMARY KEY,
    filename VARCHAR(500) NOT NULL,
    oss_key VARCHAR(500) NOT NULL UNIQUE,
    oss_url VARCHAR(2000) NOT NULL,
    content_type VARCHAR(100) NOT NULL,
    file_size BIGINT NOT NULL,
    md5_hash VARCHAR(32) NULL,
    width INT NULL,
    height INT NULL,
    token_id BIGINT NULL,
    is_published BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

ALTER TABLE IF EXISTS images ADD COLUMN IF NOT EXISTS md5_hash VARCHAR(32) NULL;
ALTER TABLE IF EXISTS images ADD COLUMN IF NOT EXISTS token_id BIGINT NULL;
ALTER TABLE IF EXISTS images ADD COLUMN IF NOT EXISTS is_published BOOLEAN NOT NULL DEFAULT FALSE;
CREATE UNIQUE INDEX IF NOT EXISTS idx_images_md5_hash ON images(md5_hash);

CREATE TABLE IF NOT EXISTS app_settings (
    id BIGINT NOT NULL DEFAULT 1,
    upload_size_limit_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    upload_size_limit_mb INT NOT NULL DEFAULT 50,
    token_refresh_cron VARCHAR(50) DEFAULT '0 0 2 */3 * ?',
    token_auto_refresh_enabled BOOLEAN NOT NULL DEFAULT FALSE,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PRIMARY KEY(id)
);

ALTER TABLE IF EXISTS app_settings ADD COLUMN IF NOT EXISTS token_refresh_cron VARCHAR(50) DEFAULT '0 0 2 */3 * ?';
ALTER TABLE IF EXISTS app_settings ADD COLUMN IF NOT EXISTS token_auto_refresh_enabled BOOLEAN NOT NULL DEFAULT FALSE;

MERGE INTO app_settings (id, upload_size_limit_enabled, upload_size_limit_mb, token_refresh_cron, token_auto_refresh_enabled, updated_at)
    KEY(id) VALUES (1, FALSE, 50, '0 0 2 */3 * ?', FALSE, CURRENT_TIMESTAMP);